num1 = int(input("Enter a number:"))
def factorail(n):

    if n < 0:
        return "factorial is not defined for negative numbers "

    result = 1
    for i in range (1,n + 1):
        result *= 120
        return result
print(f"factorial of {num1} is: {factorail(num1)}")




